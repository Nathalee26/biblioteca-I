    //create a method to add the list of users to the table
    private void agregarUsuariosTable() {
        DefaultTableModel model = (DefaultTableModel) TableUsuarios.getModel();
        model.setRowCount(0);
        Usuario.listaDeUsuarios.forEach(usuario -> {
            model.addRow(new Object[]{usuario.getId(), usuario.getNombre(),
                usuario.getApellido(), usuario.getDpi(),
                usuario.getTelefono(), usuario.getDireccion(),
                usuario.getEmail()});
        });
    }

    private void agregarLibrosTable() {
        DefaultTableModel model = (DefaultTableModel) tableLibros.getModel();
        model.setRowCount(0);
        Libro.listaDeLibros.forEach(libro -> {
            model.addRow(new Object[]{libro.getIsbn(), libro.getTitulo(),
                libro.getAutor(), libro.getAnioPublicacion(),
                libro.getEditorial(), libro.getCantidadDisponible()});
        });
    }

    private void agregarLibrosPrestadosNoDevueltosTable() {
        DefaultTableModel model = (DefaultTableModel) tableListDeLibrosPrestados.getModel();
        model.setRowCount(0);
        Prestamo.listaDePrestamos.forEach(prestamo -> {
            if(prestamo.getFechaDevolucion() ==null) {
                Libro libro = libroControlador.executeRead(prestamo.getLibroISBN());
                Usuario usuario = usuarioControlador.executeRead(prestamo.getUsuarioID());
                model.addRow(new Object[]{prestamo.getId(), prestamo.getUsuarioID(),
                        usuario.getNombre(), prestamo.getLibroISBN(), libro.getTitulo(), prestamo.getFechaPrestamo(),
                        prestamo.getFechaVencimiento()});
            }
        });

    }
