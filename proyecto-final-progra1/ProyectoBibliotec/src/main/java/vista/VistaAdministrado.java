    private void jButton6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton6MouseClicked
        // TODO add your handling code here:

        DefaultTableModel model = (DefaultTableModel) tableLibros.getModel();
        int selectedRow = tableLibros.getSelectedRow();
        int isbn = (int) model.getValueAt(selectedRow, 0);

        boolean existePrestamo = Prestamo.listaDePrestamos.stream().anyMatch(prestamo -> prestamo.getLibroISBN() == isbn);

        if (existePrestamo) {
            JOptionPane.showMessageDialog(null, "No se puede eliminar el libro, ya que tiene prestamos asociados");
            return;
        }
        libroControlador.executeDelete(isbn);
        Libro.listaDeLibros.clear();
        Libro.listaDeLibros.addAll(libroControlador.executeReadAll());
        agregarLibrosTable();
        JOptionPane.showMessageDialog(null, "Libro Eliminado con exito!!!!");
    }//GEN-LAST:event_jButton6MouseClicked

